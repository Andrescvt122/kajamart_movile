class Customer {
  final String id;
  final String nombre;
  final String tipoDocumento;
  final String numeroDocumento;
  final String email;
  final String telefono;

  const Customer({
    required this.id,
    required this.nombre,
    required this.tipoDocumento,
    required this.numeroDocumento,
    required this.email,
    required this.telefono,
  });
}
